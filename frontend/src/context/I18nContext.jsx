const I18nContext = createContext(null);

const readStoredLocale = () => {
    try {
        const raw = localStorage.getItem(STORAGE_KEY) || 'en';
        return SUPPORTED_UI_LOCALES.includes(raw) ? raw : 'en';
    } catch (error) {
        return 'en';
    }
};

export const I18nProvider = ({ children }) => {
    const [locale, setLocaleState] = useState(readStoredLocale);

    const setLocale = (nextLocale) => {
        const safeLocale = dictionary[nextLocale] && SUPPORTED_UI_LOCALES.includes(nextLocale)
            ? nextLocale
            : 'en';
        setLocaleState(safeLocale);
        localStorage.setItem(STORAGE_KEY, safeLocale);
    };

    const value = useMemo(() => ({
        locale,
        setLocale,
        t: (key) => dictionary[locale]?.[key] || dictionary.en[key] || key
    }), [locale]);

    return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
    const context = useContext(I18nContext);
    if (!context) {
        throw new Error('useI18n must be used within I18nProvider');
    }
    return context;
};

